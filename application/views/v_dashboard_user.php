    '</div>';
    });
    $("#tampil_kamar").html(datanya);
  });

  $("#cari").on('keyup',function(){
    $.getJSON("<?=base_url()?>index.php/get_kamar/cari/"+$("#cari").val(),
      function(data){
        var datanya="";
        $.each(data,function(key,dt){
          datanya+=
      '<div class="col-xs-6 col-md-3"><a href="#detail_kamar"'+
      'class="thumbnail text-center" data-toggle="modal" onclick="tm_detail('+dt['id_kamar']+')" style="text-decoration:none">'+
        '<img style="height:100px" src="<?=base_url('assets/gambar/')?>'+dt['gambar']+'" alt="...">'+
      dt['nama_kamar']+'<br>'+dt['harga']+'</a>'+
    '</div>';
        });
    $("#tampil_kamar").html(datanya);
    });
  });

  function tm_detail(id_kamar){
    $.getJSON("<?=base_url()?>index.php/Get_kamar/detail/"+id_kamar,function(data){
      $("#gambar").html(
        '<img src="<?=base_url()?>assets/gambar/'+data['gambar']+'" style="width:100%">'
      );
      $("#deskripsi").html(
      '<table class="table table-hover tabel-striped">'+
      '<tr><td>Nama Kamar</td><td>'+data['nama_kamar']+'</td></tr>'+
      '<tr><td>Harga Kamar</td><td>'+data['harga']+'</td></tr>'+
      '<tr><td>Kategori</td><td>'+data['nama_kategori']+'</td></tr>'+
      '</tabel>'
      );
      $("#jumlah").html(
        '<input type="number" id="jumlah_item" value="1" class="form-control">'
      );
      $("#btn").html('<button id="beli" onclick="beli('+data['id_kamar']+')" class="btn btn-info">Beli</button>'+
        '<a href="<?=base_url()?>index.php/trans" class="btn btn-danger" style="margin-left: 10px;">Check Out</a>');
    });
  }
  function beli(id_kamar) {
    var jumlah=$("#jumlah_item").val();
    $("#pesan").hide();
    $("#pesan").removeClass("alert alert-success");
    $.getJSON("<?=base_url()?>index.php/trans/tambah_cart/"+id_kamar+"/"+jumlah,function(hasil) {

      $("#cart").html(hasil['total_cart']);
      $("#pesan").html("Pesanan anda telah ditambahkan ke Cart");
      $("#pesan").addClass('alert alert-success');
      $("#pesan").show('animate');
      setTimeout(function(){
        $("#pesan").hide('fade');
      }, 3000);
    });
  }
  
</script>